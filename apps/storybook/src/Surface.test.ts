describe('Surface', () => {
  const storyPath = 'Core/Surface';
  const tests = [
    'Basic',
    'Custom',
    'Overflow With Button Footer',
    'No Padding',
    'Empty State',
  ];

  tests.forEach((testName) => {
    it(testName, function () {
      const id = Cypress.storyId(storyPath, testName);
      cy.visit('iframe', { qs: { id } });
      cy.compareSnapshot(testName);
    });
  });
});
